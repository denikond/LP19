#Напишите функцию hello_user(), которая с помощью функции input() 
# спрашивает пользователя “Как дела?”, пока он не ответит “Хорошо”

def hello_user():
    str1=input("Как дела?\n")
    while str1 != "Хорошо":
        str1=input("Как дела?\n")

hello_user()

