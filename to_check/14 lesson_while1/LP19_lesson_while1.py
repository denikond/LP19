qa = {"Как дела?": "Хорошо!", "Что делаешь?": "Программирую"}

#Напишите функцию ask_user() которая с помощью функции input() просит 
#пользователя ввести вопрос, а затем, если вопрос есть в словаре, 
#программа давала ему соотвествующий ответ. Например:
#Пользователь: Что делаешь?
#Программа: Программирую
def ask_user(qa):
    str1=input("Спроси меня? Как дела?/Что делаешь?\n")
    ans=qa.get(str1)
    if ans != None:
        return ans
    else:
        return "Ввод неопознан"

print (ask_user(qa))